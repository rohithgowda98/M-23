class Main {
    public static void main(String[] args) {
        // using for loop
        for (int i = 10; i > 1; i--) {
            System.out.println("The value of i is: " + i);
        }

        // using while loop
        int j = 0;
        while (j < 11) {
            System.out.println("The value of i is: " + j);
            j++;
        }
    }
}
